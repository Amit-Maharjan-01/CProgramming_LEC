//wap to calculate the volume of pool.The equation for determining the volume is
// Volume = length * width * depth. Assume that the pool has a length of 25 feet,
//a width of 10 feet,and depth of 6 feet.

 #include<stdio.h>

 void main()
 {
    int v, l = 25, w = 10, d = 6;

    v = l*w*d;

    printf("The volume of given pool is %d", v);
 }